public class l_OperasiPerbandingan {
    public static void main(String[] args) {

        // menghasilkan data boolean, true dan false
        System.out.println(5 >= 10); // 5 lebih besar atau sama dengan 10 adalah SALAH
        System.out.println(5 <= 10); // 5 lebih kecil atau sama dengan 10 adalah BENAR
        System.out.println(5 == 5); // 5 sama dengan 5 adalah BENAR
        System.out.println(5 != 5); // 5 tidak sama dengan 5 adalah SALAH

    }
}
