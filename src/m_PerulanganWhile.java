public class m_PerulanganWhile {
    public static void main(String[] args) {
        // while (kondisi)
        // jika kondisi adalah TRUE blok perulangan akan dijalankan, jika FALSE perulangan tidak dijalankan.

        byte i = 20;
        while (i >= 10) {
            System.out.println("i = " + i);
            i--;
        }
    }
}
